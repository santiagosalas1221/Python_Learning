
# Vars in Python

print("Hola, VARS")

my_name = "Santiago"
my_lastName = "Salas"
my_age = 25

print(my_name + " " + my_lastName, my_age)
print(my_age)

# Las variables pueden ser re-asignadas en otro momento aunque existan antes
my_name = "Nicolas"
print("Aqui cambia el valor", my_name)
print(my_name)

# input, para capturar datos desde la terminal

my_name = input("Cual es el nombre?")
