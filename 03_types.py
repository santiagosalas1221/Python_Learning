# Tipos de datos en Python

# String
my_name = "Nicolas"
my_name = 'Santiago'
print("Name", my_name)

# Funcion typoe que nos indica el tipo de la variable 'str' = String
print(type(my_name))

# Int
my_age = 25
print("Edad ", my_age)
print(type(my_age))

# Boolean
is_single = True
is_single = False
print("Tipo ", is_single)
print(type(is_single))

# inputs, no importa que valor se lea siempre lo tomara como un string
my_age = input("Cual es la edad: ")
print("my_age", my_age)
print(type(my_age))

